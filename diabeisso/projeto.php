       <h1>Projeto</h1>

     <p id="introdução">Introdução:
O termo diabeisso é usado para demonstrar estranhamento ou dúvida nos diálogos
nordestinos. Fazendo alusão a essa expressão, nomeamos o nosso projeto. Que consistindo
em um site para web, traz consigo a proposta de um dicionário online voltado para pessoas que
são desconhecedoras de tais palavras.</p> <br>

<p id="objetivo">Objetivo Geral:
Com o objetivo de exaltar a cultura nordestina, esse projeto foi idealizado e desenvolvido
pelos alunos do 3o ano do curso de informática. A partir de uma perspectiva sociocultural visa-
se apresentar, de forma singular, algumas das várias gírias e expressões que integram a
identidade da região Nordeste.</p> <br>

<p id="especifico">Objetivos específicos:
• Proporcionar a população brasileira uma experiência de contato com o dialeto
nordestino.
• Valorizar os aspectos regionais que fundamentam as raízes identitárias da cultura do
Nordeste.
• Destacar o papel juvenil da construção de uma nação mais justa, onde atitudes
xenofóbicas são mitigadas.</p> <br>

<p id="desenvolvimento">Desenvolvimento do Projeto:
O presente projeto foi desenvolvido por estudantes do curso de Técnico em Informática,
durante as aulas da disciplina laboratório web da grade curricular técnica, com auxílio do
professor Sandro Elias Pórfiro, usando linguagem de marcação de texto html, php, css e banco
de dados, pesquisamos palavras e expressões mais usadas aqui na região nordeste.</p> <br>

<p id="conclusao">Conclusão:
Tendo em vista o caminho percorrido durante o desenvolvimento do projeto, concluímos
que os objetivos foram alcançados. Desse modo, evidencia-se na nossa sociedade que o
protagonismo estudantil em conjunto com as ferramentas tecnológicas são essenciais para a
concretização de ideias inovadoras no âmbito sociocultural.</p>
